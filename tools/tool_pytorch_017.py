def corr2d(X, K):
    """计算二维互相关运算"""
    h, w = K.shape
    Y = tpy.zeros((X.shape[0] - h + 1, X.shape[1] - w + 1))
    for i in range(Y.shape[0]):
        for j in range(Y.shape[1]):
            Y[i, j] = tpy.reduce_sum((X[i: i + h, j: j + w] * K))
    return Y

